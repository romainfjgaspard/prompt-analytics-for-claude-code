    timestamp: str
    model: str
    stop_reason: str
    is_sidechain: int
    post_compact: int
    input_tokens: int
    output_tokens: int
    cache_read_tokens: int
    cache_write_5m_tokens: int
    cache_write_1h_tokens: int
    server_tool_use_requests: int
