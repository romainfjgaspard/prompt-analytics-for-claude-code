# CSV column layouts.
# ---------------------------------------------------------------------------

SESSIONS_COLS = ["session_id", "start_date", "project", "cwd", "git_branch"]

PROMPTS_COLS = [
    "session_id",
    "prompt_id",
    "prompt_index",
    "timestamp",
    "project",
    "cwd",
    "git_branch",
    "mode",
    "entrypoint",
    "version",
    "model",
    "char_count",
    "assistant_turns",
    "tool_calls",
    "final_stop_reason",
    "prompt_preview",
]

# Raw counts only (D3). ``session_id`` is denormalized so that usage attached
# to pseudo-prompts (continuations, filtered meta prompts) stays attributable
# to a session even without a matching prompts.csv row. ``model`` is carried at
# this grain (not joined from prompts.csv) because pricing is per model: a
# prompt can mix models (e.g. Haiku subcalls) and pseudo-prompts have no
# prompts.csv row at all -- both would make read-time costs wrong otherwise.
# ``is_sidechain`` (0/1) splits subagent usage out as a first-class dimension
# (plan v2 1.2); summing over it always reproduces the per-prompt totals (V7).
TOKENS_COLS = ["session_id", "prompt_id", "model", "token_type", "is_sidechain", "token_count"]

# Request grain (plan v2 1.1, decision V10: always written). One row per
# deduplicated API request (assistant message), token counts pivoted; rows
# carrying no usage at all (e.g. synthetic notices) are skipped. Per prompt,
# the column sums equal the prompt's tokens.csv totals exactly (V7) -- this is
# the substrate of the TTL / compaction / accumulated-context analyses.
# ``request_index`` is the request's 1-based chronological position within its
# prompt; ``post_compact`` (0/1) marks requests answering the synthetic
# post-compaction continuation (1.4).
REQUESTS_COLS = [
    "session_id",
    "prompt_id",
    "request_index",
    "timestamp",
    "model",
    "stop_reason",
    "is_sidechain",
    "post_compact",
    "input_tokens",
    "output_tokens",
    "cache_read_tokens",
    "cache_write_5m_tokens",
    "cache_write_1h_tokens",
    "server_tool_use_requests",
]

TOKEN_TYPES_COLS = ["token_type", "label", "description"]

PROMPT_TEXT_COLS = ["prompt_id", "prompt_text"]

CATEGORIES_COLS = [
    "prompt_id",
    "category",
    "complexity",
    "classifier_model",
    "classified_at",
]

QUOTA_LOG_COLS = ["snapshot_at", "field", "utilization_pct", "resets_at"]

# Output composition (Axe C). ``output_files.csv`` is long: one row per
# ``(prompt_id, language, kind)`` so a multi-language prompt is preserved, in
# the spirit of ``tokens.csv``. ``files`` counts distinct files touched in the
# group; the line counts are exact LCS diffs. Metrics only -- no source code,
# no file paths, ever reach this CSV.
OUTPUT_FILES_COLS = ["prompt_id", "language", "kind", "files", "lines_added", "lines_deleted"]

# Per-prompt prose/code split of the generated output tokens (Axe C). The real
# ``output_tokens`` are prorated by the local-tokenizer weight of the message's
# text vs tool_use blocks; per prompt the two columns sum to the prompt's total
# output tokens in ``tokens.csv`` (estimate on the split, exact on the total).
OUTPUT_TOKENS_COLS = ["prompt_id", "output_prose_tokens", "output_code_tokens"]

# Context composition (Axe D, static snapshot). Long: one row per
# ``(session_id, source, language)``. ``source`` is one of the four context
# buckets (``conversation`` / ``file_read`` / ``tool_output`` / ``config``);
# ``language`` is the file language for ``file_read`` rows (``-`` otherwise).
# ``tokens`` is the local-tokenizer size of every distinct piece of that content
# the session ever put in context, and ``items`` counts those pieces (distinct
# tool results, attachments, prompts + assistant turns). A single local
# tokenizer measures every source, so the per-source *share* is an honest ratio
# (the API never reports a per-element token count). Metrics only -- no content,
# no file paths, ever reach this CSV.
CONTEXT_SOURCES_COLS = ["session_id", "source", "language", "tokens", "items"]


def continuation_prompt_id(session_id: str) -> str:
    """Pseudo prompt_id gathering a session's unattributable usage.

    Assistant events that cannot be tied to any user prompt (e.g. the tail of
    a resumed conversation replayed before its first prompt) are counted under
    this id: present in ``tokens.csv``, absent from ``prompts.csv``.
    """
    return f"{session_id}:_continuation"


# ---------------------------------------------------------------------------
# Parser <-> aggregation shapes.
#
# These are TypedDicts (not dataclasses) on purpose: parsed files are cached
# on disk as JSON keyed by file mtime/size, so the shapes must round-trip
# through ``json`` without custom encoders.
# ---------------------------------------------------------------------------


class ParsedPrompt(TypedDict):
    """A real human prompt found in a session file."""

    prompt_id: str
    timestamp: str
    cwd: str
    git_branch: str
    mode: str
    entrypoint: str
    version: str
    text: str
    # Local-tokenizer size of ``text`` (Axe D): the prompt's contribution to the
    # ``conversation`` context source, counted at parse time so it is cached.
    text_tokens: int


class ToolEdit(TypedDict):
    """Metrics derived from one file-editing ``tool_use`` block (Axe C).

    Carries no source code: only the derived language/kind, the exact line
    diff, and a project-relative path kept solely to count *distinct* files
    (never written to any CSV).
    """

    tool_id: str
    path: str
    language: str
    kind: str
    lines_added: int
    lines_deleted: int


class ContextItem(TypedDict):
    """One piece of content that entered the context, sized in tokens (Axe D).

    Emitted for tool results (file reads, command/search output) and injected
    attachments (skill/tool listings, file references). The dialogue itself
    (prompts + assistant turns) is *not* an item -- it is summed from the
    prompts and the deduplicated usage records, which already carry their token
    weights. Carries no content: only the source bucket, the (file) language,
    the local-tokenizer ``tokens`` size, the prompt + ``post_compact``
    membership reconstructed from the ``parentUuid`` chain, and a ``dedup_id``
    (the tool-use id or attachment uuid) so a replayed session is not
    double-counted.
    """

    prompt_id: str
    post_compact: bool
    source: str
    language: str
    tokens: int
    dedup_id: str


class UsageRecord(TypedDict):
    """One assistant JSONL line carrying API usage.

    ``prompt_id`` is resolved at parse time from the event's own ``promptId``
    or, in newer formats, by walking the ``uuid``/``parentUuid`` chain back to
    a user prompt. Empty when unresolvable (continuation tail).

    ``dedup_key`` combines ``message.id`` and ``requestId``; the aggregation
    keeps the largest usage snapshot per key globally across all files (a
    message is written as several lines with progressive usage, and resumed
    sessions replay the same records into new files).

    ``post_compact`` is True when the record descends (via the
    ``uuid``/``parentUuid`` chain) from a synthetic post-compaction
    continuation message, until the next real human prompt breaks the chain.

    ``prose_tokens``/``code_tokens`` are local-tokenizer weights of this
    message's ``text`` vs ``tool_use`` blocks (Axe C); the aggregation prorates
    the real ``output_tokens`` by them. ``file_edits`` holds the per-edit
    metrics for any file-editing tool calls on this line (deduplicated by
    ``tool_id`` at aggregation time, like ``tool_use_ids``).
    """

    prompt_id: str
    dedup_key: str
    timestamp: str
    model: str
    stop_reason: str
    is_sidechain: bool
    post_compact: bool
    tokens: dict[str, int]
    tool_use_ids: list[str]
    prose_tokens: int
    code_tokens: int
    file_edits: list[ToolEdit]


class ParsedFile(TypedDict):
    """The cacheable result of parsing one JSONL file (no filtering applied)."""

    session_id: str
    cwd: str
    git_branch: str
    first_timestamp: str
    prompts: list[ParsedPrompt]
    usage: list[UsageRecord]
    context_items: list[ContextItem]
    lines_total: int
    lines_invalid: int
    event_types: dict[str, int]
    filtered_prompts: dict[str, int]
    versions: list[str]


