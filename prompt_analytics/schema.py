"""Canonical data contract for prompt-analytics CSV outputs.

Single source of truth for:

* the machine-readable ``token_type`` keys (used as join keys everywhere),
* the column layout of every CSV written by :mod:`prompt_analytics.extract`
  (and consumed by the analytics layer and the dashboard),
* the typed shapes exchanged between the parser and the aggregation step.

Design decisions (see ``docs/PLAN.md``, D2/D3):

* ``tokens.csv`` stores **raw counts only** -- no prices, no costs. Costs are
  computed at read time from the pricing tables, so a pricing update never
  requires a re-extract.
* ``token_type`` is a machine key (``input``, ``cache_write_5m``, ...);
  display labels live in ``token_types.csv``.
* LLM categorization lives in its own ``categories.csv`` keyed by
  ``prompt_id`` -- extract never reads nor writes it.
"""

